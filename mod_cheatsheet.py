console = Console()

class T:
    CYAN='bold bright_cyan'; MAG='bold bright_magenta'; YEL='bold bright_yellow'
    GRN='bold bright_green'; RED='bold bright_red'; WHT='bold bright_white'
    ACCENT='bold bright_magenta'; DIM='dim bright_white'; OK='bold bright_green'
    WARN='bold bright_yellow'; KEY='bold bright_yellow'; LABEL='bold bright_white'
    DESC='dim bright_white'

def read_key():
    ch = msvcrt.getch()
    if ch == b'\r': return ('enter','')
    if ch == b'\x1b': return ('esc','')
    if ch == b'\x03': raise KeyboardInterrupt
    if ch in (b'\x00', b'\xe0'):
        ch2 = msvcrt.getch()
        m = {b'H':'up',b'P':'down',b'I':'pgup',b'Q':'pgdn'}
        return ('nav', m.get(ch2,''))
    return ('char', ch.decode('utf-8',errors='ignore').lower())

def clear(): os.system('cls')

def scroll_view(title, lines):
    pos = 0
    page = max(console.height - 10, 8)
    while True:
        clear()
        console.print(Align.center(Text(f"  ═══  {title}  ═══  ", style=T.ACCENT)))
        console.print()
        visible = lines[pos:pos+page]
        for line in visible:
            if isinstance(line, Text):
                console.print(f"  {line}")
            else:
                console.print(f"  {line}")
        total = len(lines)
        if total > page:
            pct = int(pos/max(1,total-page)*100)
            bar_len = 40
            filled = int(bar_len*pos/max(1,total-page))
            bar = '█'*filled + '░'*(bar_len-filled)
            console.print(f"\n  [{T.DIM}]  {bar}  {pct}%[/]")
        console.print(f"  [{T.DIM}]↑↓ 滚动  PgUp/PgDn 翻页  Esc 返回[/]")
        kt,k = read_key()
        if kt=='esc' or (kt=='char' and k=='q'): return
        if kt=='nav':
            if k=='up' and pos>0: pos-=1
            if k=='down' and pos<max(0,total-page): pos+=1
            if k=='pgup': pos=max(0,pos-page)
            if k=='pgdn': pos=min(max(0,total-page),pos+page)

def fmt(data):
    lines = []
    for item in data:
        if len(item)==3 and item[2]=='h':
            lines.append(Text(f"\n  {item[0]}", style=T.ACCENT))
        else:
            t = Text()
            t.append(f"  {item[0]:<36}", style=T.GRN)
            t.append(item[1], style=T.DESC)
            lines.append(t)
    return lines

