def cheatsheet_menu():
    while True:
        clear()
        console.print(Align.center(Text("  ═══  HYPERVOID 速查表  ═══  ", style=T.ACCENT)))
        console.print()
        for key, name, _ in SHEETS:
            console.print(f"  [{T.KEY}][{key}][/] [{T.LABEL}]{name}[/]")
        console.print()
        console.print(f"  [{T.DIM}]选择速查表  Esc 返回[/]")
        console.print(f"  [{T.DIM}]hypervoid.top[/]")

        kt,k = read_key()
        if kt=='esc' or (kt=='char' and k=='q'): return
        if kt=='char' and k in [str(i) for i in range(10)]:
            idx_map = {'1':0,'2':1,'3':2,'4':3,'5':4,'6':5,'7':6,'8':7,'9':8,'0':9}
            if k in idx_map:
                idx = idx_map[k]
                if idx < len(SHEETS):
                    _,name,data = SHEETS[idx]
                    scroll_view(name, fmt(data))

if __name__ == '__main__':
    cheatsheet_menu()
